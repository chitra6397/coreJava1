package udemyJavaProgrammingBeginner;

public class NewVariable {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        int c=30;
        System.out.printf("a + b + c = a+b+c").println();
        System.out.printf("%d + %d +%d = %d" ,a,b,c,a+b+c).println();
        int a2=50;
        System.out.printf("%d+ %d +%d =%d",a2,b,c,a2+b+c).println();
        int b2=60;
        System.out.printf("%d + %d + %d=%d",a2,b2,c,a2+b2+c).println();
    }
}
