public class SwitchVowels {
    public static void main(String[]args){
        char ch ='o';
        switch (ch){
            case 'a':
                System.out.println("Vowels");
            break;
            case 'e':
                System.out.println("Vowels");
                break;
            case 'o':
                System.out.println("vewels");
                break;
            case 'u':
                System.out.println("Vowels");
                break;
            case 'v':
                System.out.println("vowels");
                break;
            default:
                System.out.println("Consonant");
                }


        }
    }

